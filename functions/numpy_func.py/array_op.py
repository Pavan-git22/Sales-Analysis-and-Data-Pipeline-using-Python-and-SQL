import numpy as np
arr = np.array([1, 2, 3, 4, 5])
print(arr+5)
print(arr*2)
print(arr/2)
print(arr**2)

print(np.sum(arr))
print(np.mean(arr))
print(np.median(arr))
print(np.std(arr)) #Standard deviation is the square root of the variance, providing a measure of the spread of the dataset in the same units as the data.
print(np.var(arr)) #Variance measures the dispersion of a dataset, indicating how much the values differ from the mean
print(np.min(arr))
print(np.max(arr))

